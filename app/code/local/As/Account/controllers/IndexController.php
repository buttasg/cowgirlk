            $this->_redirect('*/*/');
            return;
        }
        //$this->getResponse()->setHeader('Login-Required', 'true');
        //$this->loadLayout();
        //$this->_initLayoutMessages('customer/session');
        //$this->_initLayoutMessages('catalog/session');
        //$this->renderLayout();
